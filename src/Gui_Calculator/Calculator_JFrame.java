    private void Button_EqualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_EqualActionPerformed
        // TODO add your handling code here:
         LastNumber =  Integer.parseInt(Display_TextField1.getText()); 
        switch(operation){
            case "+":
                Result = FirstNumber + LastNumber;
                Display_TextField1.setText(String.valueOf(Result));
                break;
            case "-":
                Result = FirstNumber - LastNumber;
                Display_TextField1.setText(String.valueOf(Result));
                break;
            case "*":
                Result = FirstNumber * LastNumber;
                Display_TextField1.setText(String.valueOf(Result));
                break;
            case "/":
                if(LastNumber == 0){
                    Display_TextField1.setText("Math error!");
                }else{
                Result = FirstNumber / LastNumber;
                Display_TextField1.setText(String.valueOf(Result));
                }
                break;
            case "%":
                Result = FirstNumber % LastNumber;
                Display_TextField1.setText(String.valueOf(Result));
                break;
            case "âˆš":
               
                Display_TextField1.setText(String.valueOf(Math.sqrt(LastNumber)));
                
               default:
                   Display_TextField1.setText("No operation!");
                
        }
    }//GEN-LAST:event_Button_EqualActionPerformed

